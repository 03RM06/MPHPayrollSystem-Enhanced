    private void jButtonDeleteRecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteRecActionPerformed
        int selectedRow = jTableDataBase.getSelectedRow();
        if (selectedRow != -1) {

            String idString = jTableDataBase.getValueAt(selectedRow, 0).toString();

            int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete Employee #" + idString + "?",
                "Confirm Deletion", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    int id = Integer.parseInt(idString);

                    EmployeeDAO dao = new EmployeeDAO();

                    boolean ok = dao.delete(id);

                    if (ok) {
                        JOptionPane.showMessageDialog(this, "Employee deleted successfully.");
                        jButtonRefreshActionPerformed(null); // Refresh the table
                    } else {
                        JOptionPane.showMessageDialog(this, "Delete failed. Employee might not exist.");
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid ID format in table.");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select an employee to delete.");
        }
    }//GEN-LAST:event_jButtonDeleteRecActionPerformed

