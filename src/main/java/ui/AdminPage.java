    private void jTableDataBaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDataBaseMouseClicked
        DefaultTableModel model = (DefaultTableModel) jTableDataBase.getModel();
        int SelectedRowIndex = jTableDataBase.getSelectedRow();

        jTextFieldEmpNum.setText(model.getValueAt(SelectedRowIndex, 0).toString());
        jTextFieldLastName.setText(model.getValueAt(SelectedRowIndex, 1).toString());
        jTextFieldFirstName.setText(model.getValueAt(SelectedRowIndex, 2).toString());
        jTextAreaAddress.setText(model.getValueAt(SelectedRowIndex, 4).toString());
        jTextFieldPhoneNum.setText(model.getValueAt(SelectedRowIndex, 5).toString());
        jTextFieldSupervisor.setText(model.getValueAt(SelectedRowIndex, 12).toString());
        jTextFieldPosition.setText(model.getValueAt(SelectedRowIndex, 11).toString());
        jTextFieldStatus.setText(model.getValueAt(SelectedRowIndex, 10).toString());

        boolean isAdmin = SessionManager.getInstance().getCurrentUser().getRole() == Model.Role.ADMIN;

        if(isAdmin || SessionManager.getInstance().canAccessFeature("EDIT_EMPLOYEE")) {
            jButtonEditRec.setEnabled(true);
        }
        if(isAdmin || SessionManager.getInstance().canAccessFeature("DELETE_EMPLOYEE")) {
            jButtonDeleteRec.setEnabled(true);
        }
        if(isAdmin || SessionManager.getInstance().canAccessFeature("VIEW_ALL_PAYSLIPS")) {
            jButtonViewEmp.setEnabled(true);
        } else {
            jButtonViewEmp.setEnabled(false);
        }

        jButtonClear.setEnabled(true);
    }//GEN-LAST:event_jTableDataBaseMouseClicked

    private void jButtonViewEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonViewEmpActionPerformed

        String selectedId = jTextFieldEmpNum.getText();
        if (selectedId == null || selectedId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select an employee first.");
            return;
        }

        try {
            EmployeeDAO employeeDAO = new EmployeeDAO();
            Model.Employee emp = employeeDAO.findById(selectedId);

            if (emp == null || emp.getBasicSalary() == null || emp.getBasicSalary().compareTo(BigDecimal.ZERO) == 0) {
                JOptionPane.showMessageDialog(
                    this,
                    "This employee is new / no current payroll record exists in the system.",
                    "No Record Found",
                    JOptionPane.INFORMATION_MESSAGE
                );
                return;
            }

            UserAccount user = new UserAccount();
            user.setEmployeeID(selectedId);

            ViewEmployee viewEmployee = new ViewEmployee(user, this);
            viewEmployee.setVisible(true);
            this.setVisible(false);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                this,
                "Error loading employee: " + ex.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButtonViewEmpActionPerformed

